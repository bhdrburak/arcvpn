package com.wireguard.android.presentation.qr_setup

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.Fragment
import androidx.navigation.fragment.findNavController
import com.wireguard.android.R
import com.wireguard.android.databinding.FragmentQrSetupBinding


class QRSetupFragment : Fragment() {

    private var _binding: FragmentQrSetupBinding? = null
    private val binding get() = _binding!!

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

    }

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        _binding = FragmentQrSetupBinding.inflate(inflater, container, false)
        return binding.root
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)

        binding.appBar.ibAction.setImageResource(R.drawable.ic_close)
        binding.appBar.ibAction.setOnClickListener {
            findNavController().navigateUp()
        }
    }

}